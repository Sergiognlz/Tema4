package parte3.ejercicio1;

public class Suma {

	/**
	 * Diseña una clase que contenga dos funciones de nombre suma que hagan lo
	 * siguiente: 
	 * La primera de las funciones recibirá dos argumentos de tipo entero
	 * y devolverá la suma de ambos. El valor a devolver también será entero. 
	 * 
	 * La segunda de las funciones recibirá dos argumentos de tipo double y devolverá
	 * la suma de ambos. El tipo a devolver debe ser también double.
	 */
	
	
	public static  int suma(int num1, int num2) {
		int suma=num1+num2;
		
		return suma;
	}
	
	public static  double suma(double num1, double num2) {
		double suma=num1+num2;
		
		return suma;
	}
	

}
